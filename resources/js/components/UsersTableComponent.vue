<template>
    <div v-if="done">
        <h1 style="font-weight: 300">Users</h1>
        <a href="/admin/users/add" class="btn btn-success">Create user</a>
        <table class="table">
            <thead>
                <tr>
                    <th scope="col">Id</th>
                    <th scope="col">Title</th>
                    <th scope="col">Created</th>
                    <th scope="col" class="actions">Actions</th>
                </tr>
            </thead>
            <tbody>
                <tr v-for="user in users.users">
                    <td>{{ user.id }}</td>
                    <td>{{ user.email }}</td>
                    <td>{{ user.created }}</td>
                    <td>
                        <a :href="'/users/view/' + user.id">View</a>
                        <a :href="'/admin/users/edit/' + user.id">Edit</a>
                    </td>
                </tr>
            </tbody>
        </table>
    </div>
</template>

<script>
    import axios from 'axios'
    export default {
        mounted () {
            console.log('Component PostsTableComponent mounted.')
        },
        data () {
            return {
                done: false,
                users: {
                    users: []
                },
                errors: []
            }
        },
        created () {
            this.$parent.$emit('pageLoader', true)
            axios.get('/api/users.json')
            .then(response => {
                this.users = response.data
                this.$parent.$emit('pageLoader', false)
                this.done = true
            })
            .catch(e => {
                this.errors.push(e)
            })
        }
    }
</script>
